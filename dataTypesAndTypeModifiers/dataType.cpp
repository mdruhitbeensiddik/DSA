#include<bits\stdc++.h>
using namespace std;

int main()
{   
    int a;
    cout<< "Size of Integer is: "<< sizeof(a) << endl;

    float b;
    cout<< "Size of Float is: "<< sizeof(b) << endl;

    double c;
    cout<< "Size of Double is: "<< sizeof(c) << endl;

    char d;
    cout<< "Size of Character is: "<< sizeof(d) << endl;

    bool e;
    cout<< "Size of Bool is: "<< sizeof(e) << endl;

    return 0;
}